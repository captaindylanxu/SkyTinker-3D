export function detectLanguage() {
  const browserLang = navigator.language || navigator.userLanguage || 'en';
  
  // ç²¾ç¡®åŒ¹é…
  if (LOCALES[browserLang]) {
    return browserLang;
  }
  
  // ç®€ä½“ä¸­æ–‡å˜ä½“
  if (browserLang.startsWith('zh-CN') || browserLang === 'zh-Hans' || browserLang === 'zh') {
    return 'zh-CN';
  }
  
  // ç¹ä½“ä¸­æ–‡å˜ä½“
  if (browserLang.startsWith('zh-TW') || browserLang.startsWith('zh-HK') || browserLang === 'zh-Hant') {
    return 'zh-TW';
  }
  
  // è¯­è¨€ä»£ç å‰ç¼€åŒ¹é…
  const langPrefix = browserLang.split('-')[0];
  if (LOCALES[langPrefix]) {
    return langPrefix;
  }
  
  // é»˜è®¤è‹±æ–‡
  return 'en';
}

